package ncl.b1037041.exception;

@SuppressWarnings("serial")
public class AlertException extends Exception {

	public AlertException() {
		super();
	}

	public AlertException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AlertException(String arg0) {
		super(arg0);
	}

	public AlertException(Throwable arg0) {
		super(arg0);
	}
}
